#!/usr/bin/env python3
"""Generate nnU-Net compatible dataset from your processed scans.

Assumes the following directory layout:
    <processed_data>/
        <scan_id>/
            ct_scan.nii.gz          # original CT (or any .nii/.nii.gz that is not a mask)
            trachea_mask.nii.gz     # binary mask generated by the hybrid segmentor (used as ground‑truth)

The script creates the nnU-Net dataset in the location specified by the
nnUNet_raw_data_base environment variable (if set) or in the default
nnUNet_raw_data/Task001_Trachea location.

Usage:
    python3 data_preparation/nnunet_dataset.py --processed-data /path/to/processed_data
    # Or to override the base location:
    nnUNet_raw_data_base=/mnt/ssd/nnUNet_data python3 data_preparation/nnunet_dataset.py --processed-data /path/to/processed_data
"""

import os
import json
import shutil
from pathlib import Path
import argparse


def find_ct_nifti(scan_dir: Path) -> Path:
    """Return the first NIfTI file in *scan_dir* that is not a mask.
    Looks for ``.nii`` or ``.nii.gz`` files that do not contain the words
    ``mask``, ``healthy`` or ``trachea`` in the filename.
    """
    for f in sorted(scan_dir.iterdir()):
        if f.suffix in {".nii", ".gz"} and f.is_file():
            name = f.name.lower()
            if all(skip not in name for skip in ["mask", "healthy", "trachea"]):
                return f
    raise FileNotFoundError(f"No CT NIfTI found in {scan_dir}")


def main():
    parser = argparse.ArgumentParser(
        description="Generate nnU-Net dataset from processed scans"
    )
    parser.add_argument(
        "--processed-data",
        type=str,
        default="processed_data",
        help="Path to processed data directory (default: processed_data)",
    )
    args = parser.parse_args()

    # Determine output base directory:
    # 1. If nnUNet_raw_data_base environment variable is set, use it
    # 2. Otherwise, use default nnUNet_raw_data in current directory
    nnunet_raw_data_base = os.environ.get("nnUNet_raw_data_base")
    if nnunet_raw_data_base:
        OUTPUT_ROOT = Path(nnunet_raw_data_base) / "Task001_Trachea"
        print(f"Using nnUNet_raw_data_base from environment: {nnunet_raw_data_base}")
    else:
        # Default location if environment variable not set
        OUTPUT_ROOT = Path("nnUNet_raw_data") / "Task001_Trachea"
        print(f"Using default nnUNet_raw_data location: {OUTPUT_ROOT.parent}")

    PROCESSED_ROOT = Path(args.processed_data)
    TASK_NAME = "Trachea"
    TASK_ID = "001"

    # Clean previous task if it exists
    if OUTPUT_ROOT.exists():
        shutil.rmtree(OUTPUT_ROOT)
    (OUTPUT_ROOT / "imagesTr").mkdir(parents=True, exist_ok=True)
    (OUTPUT_ROOT / "labelsTr").mkdir(parents=True, exist_ok=True)

    images = []
    labels = []

    for scan_dir in sorted(PROCESSED_ROOT.iterdir()):
        if not scan_dir.is_dir():
            continue
        ct_path = None
        try:
            ct_path = find_ct_nifti(scan_dir)
        except FileNotFoundError as e:
            print(e)
            continue
        mask_path = scan_dir / "trachea_mask.nii.gz"
        if not mask_path.is_file():
            print(f"Missing mask for {scan_dir.name}, skipping")
            continue
        # nnU-Net expects filenames like "<scan_id>_0000.nii.gz" for images
        # (the trailing "_0000" is the modality index). For a single modality we
        # keep that convention.
        image_target = OUTPUT_ROOT / "imagesTr" / f"{scan_dir.name}_0000.nii.gz"
        label_target = OUTPUT_ROOT / "labelsTr" / f"{scan_dir.name}.nii.gz"
        shutil.copy(ct_path, image_target)
        shutil.copy(mask_path, label_target)
        images.append(f"imagesTr/{scan_dir.name}_0000.nii.gz")
        labels.append(f"labelsTr/{scan_dir.name}.nii.gz")
        print(f"Added scan {scan_dir.name}")

    # Create dataset.json – this is the minimal required structure
    dataset = {
        "name": f"Task{TASK_ID}_{TASK_NAME}",
        "description": "Trachea segmentation dataset generated from processed_data",
        "tensorImageSize": "3D",
        "reference": "",
        "licence": "",
        "release": "1.0",
        "modality": {"0": "CT"},
        "labels": {"0": "background", "1": "trachea"},
        "numTraining": len(images),
        "numTest": 0,
        "training": [{"image": img, "label": lbl} for img, lbl in zip(images, labels)],
        "test": [],
    }
    with open(OUTPUT_ROOT / "dataset.json", "w") as f:
        json.dump(dataset, f, indent=2)
    print("nnU-Net dataset prepared at", OUTPUT_ROOT)


if __name__ == "__main__":
    main()
